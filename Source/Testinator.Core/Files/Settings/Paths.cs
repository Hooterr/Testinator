        None,

        /// <summary>
        /// Represents the MyDocumments/Program folder
        /// </summary>
        MyDocuments,

        /// <summary>
        /// Represents the C:/ProgramFiles/ProgramName folder
        /// </summary>
        InstallationFolder,
    }
}
